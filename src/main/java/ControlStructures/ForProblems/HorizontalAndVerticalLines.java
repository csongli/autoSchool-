package ControlStructures.ForProblems;

public class HorizontalAndVerticalLines {

    public static void main(String[] args) {
        // Horizontal line
        for (int i = 0; i < 10; i++) {
            System.out.print("8");
        }
        System.out.println();

        // Vertical line
        for (int i = 0; i < 10; i++) {
            System.out.println("8");
        }
    }
}

